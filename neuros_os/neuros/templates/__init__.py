# ── 8. Remote Control ────────────────────────────────────────────────────
_t("remote-control", "Remote Control", "Control robot via keyboard input", '''
"""
{project_name} — Remote Control
Generated by: neuros new {project_name} --template remote-control

Control your robot with keyboard keys:
  W/S = forward/backward
  A/D = turn left/right
  Q   = quit
  X   = emergency stop
"""
from neuros import Robot, spin
import threading
import sys

robot = Robot("{project_name}")
robot.start()

robot.pin("MOTOR_L", pin=5, mode="pwm")
robot.pin("MOTOR_R", pin=6, mode="pwm")

speed = 0.5
current_cmd = "stop"

def keyboard_listener():
    global current_cmd
    print("\\n🎮 Remote Control Active")
    print("  W = Forward | S = Backward")
    print("  A = Left    | D = Right")
    print("  X = E-Stop  | Q = Quit\\n")

    while True:
        try:
            key = input("❯ ").strip().lower()
        except (EOFError, KeyboardInterrupt):
            break

        if key == "w":
            current_cmd = "forward"
            robot.publish("motor/cmd", {{"left": speed, "right": speed}})
            print("  ⏩ Forward")
        elif key == "s":
            current_cmd = "backward"
            robot.publish("motor/cmd", {{"left": -speed, "right": -speed}})
            print("  ⏪ Backward")
        elif key == "a":
            current_cmd = "left"
            robot.publish("motor/cmd", {{"left": -speed, "right": speed}})
            print("  ↩️ Left")
        elif key == "d":
            current_cmd = "right"
            robot.publish("motor/cmd", {{"left": speed, "right": -speed}})
            print("  ↪️ Right")
        elif key == "" or key == " ":
            current_cmd = "stop"
            robot.publish("motor/cmd", {{"left": 0, "right": 0}})
            print("  ⏹ Stop")
        elif key == "x":
            print("  🛑 EMERGENCY STOP")
            robot.stop()
            break
        elif key == "q":
            print("  👋 Quitting")
            robot.stop()
            sys.exit(0)

# Run keyboard listener in a thread
t = threading.Thread(target=keyboard_listener, daemon=True)
t.start()

spin(robot)
''')


# ── 9. Light Follower ────────────────────────────────────────────────────
_t("light-follower", "Light Follower", "Follow a light source using LDR photoresistors", '''
"""
{project_name} — Light Follower
Generated by: neuros new {project_name} --template light-follower

Hardware:
  - 2x LDR (Light Dependent Resistor) — left and right
  - 2x DC motors
  - Motor driver
"""
from neuros import Robot, Node, spin, NodePriority


class LightFollowerNode(Node):
    """Follow the brightest light source using differential drive."""

    def configure(self):
        self.hal.pin("LDR_LEFT",  board_pin=34, mode="analog_in")
        self.hal.pin("LDR_RIGHT", board_pin=35, mode="analog_in")
        self.hal.pin("MOTOR_L",   board_pin=5,  mode="pwm")
        self.hal.pin("MOTOR_R",   board_pin=6,  mode="pwm")

        self.base_speed = 0.4
        self.gain = 2.0
        self.threshold = 0.1  # minimum light to activate

    def tick(self):
        left_light  = self.hal.read("LDR_LEFT")
        right_light = self.hal.read("LDR_RIGHT")

        avg_light = (left_light + right_light) / 2

        if avg_light < self.threshold:
            # Too dark — stop
            self.hal.write("MOTOR_L", 0)
            self.hal.write("MOTOR_R", 0)
            return

        # Differential: steer towards brighter side
        error = left_light - right_light
        left_speed  = max(0, min(1, self.base_speed + error * self.gain))
        right_speed = max(0, min(1, self.base_speed - error * self.gain))

        self.hal.write("MOTOR_L", left_speed)
        self.hal.write("MOTOR_R", right_speed)

        self.publish("/robot/light", {{
            "left": round(left_light, 3),
            "right": round(right_light, 3),
            "error": round(error, 3),
        }})

    def on_emergency_stop(self, reason):
        self.hal.write("MOTOR_L", 0)
        self.hal.write("MOTOR_R", 0)
        super().on_emergency_stop(reason)


robot = Robot("{project_name}")
robot.add_node(LightFollowerNode("light_follower", hz=20, priority=NodePriority.HIGH))
robot.start()

print("💡 Light follower running — shine a flashlight to steer the robot")
spin(robot)
''')


# ── 10. Node Example ─────────────────────────────────────────────────────
_t("node-example", "Custom Node", "Example showing Node lifecycle, pub/sub, and HAL usage", '''
"""
{project_name} — Custom Node Example
Generated by: neuros new {project_name} --template node-example

Demonstrates the full Node lifecycle:
  configure() → on_activate() → tick() → destroy()
"""
from neuros import Robot, Node, spin, NodePriority
import logging


class BlinkerNode(Node):
    """Blinks an LED at a configurable rate."""

    def configure(self):
        self.hal.pin("LED", board_pin=13, mode="output")
        self.led_state = False
        self.logger = logging.getLogger(self.name)

    def on_activate(self):
        self.logger.info("BlinkerNode activated at %.1f Hz", self.hz)

    def tick(self):
        self.led_state = not self.led_state
        self.hal.write("LED", int(self.led_state))
        self.publish("/robot/actuator/led", {{"state": self.led_state}})

    def destroy(self):
        self.hal.write("LED", 0)  # turn off LED on shutdown
        super().destroy()


class StatusNode(Node):
    """Publishes system status and listens for LED events."""

    def configure(self):
        self.counter = 0
        self.led_toggles = 0
        self.subscribe("/robot/actuator/led", self.on_led)

    def on_led(self, msg):
        self.led_toggles += 1

    def tick(self):
        self.counter += 1
        self.publish("/robot/system/status", {{
            "uptime_ticks": self.counter,
            "led_toggles": self.led_toggles,
            "node_state": self.state.value,
        }})

        if self.counter % 50 == 0:
            print(f"  Status: tick={{self.counter}} led_toggles={{self.led_toggles}}")


# Create robot with multiple nodes
robot = Robot("{project_name}")
robot.add_node(BlinkerNode("blinker", hz=2, priority=NodePriority.NORMAL))
robot.add_node(StatusNode("status", hz=0.2, priority=NodePriority.LOW))
robot.start()

print("🧩 Custom node example running — 2 nodes active")
spin(robot)
''')


def get_template(name: str) -> Optional[dict]:
    """Get a template by name."""
    # Normalize: accept both "line-follower" and "line_follower"
    normalized = name.lower().replace("_", "-")
    return TEMPLATES.get(normalized)


def list_templates() -> List[dict]:
    """Return all available templates."""
    return list(TEMPLATES.values())


def render_template(template_name: str, project_name: str) -> Optional[str]:
    """Render a template with the project name substituted."""
    tmpl = get_template(template_name)
    if tmpl is None:
        return None
    return tmpl["code"].format(project_name=project_name)
