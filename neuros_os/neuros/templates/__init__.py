# ── 2. Line Follower ──────────────────────────────────────────────────────
_t("line-follower", "Line Follower", "Follow a line using IR sensors + PID control", '''
"""
{project_name} — Line Follower Robot
Generated by: neuros new {project_name} --template line-follower

Hardware required:
  - 2x DC motors (left/right)
  - 2x IR line sensors (analog)
  - Motor driver (L298N or similar)
"""
from neuros import Robot, Node, spin, NodePriority


class LineFollowerNode(Node):
    """PID-based line following with 2 IR sensors."""

    def configure(self):
        # IR sensors (analog input)
        self.hal.pin("IR_LEFT",  board_pin=2, mode="analog_in")
        self.hal.pin("IR_RIGHT", board_pin=3, mode="analog_in")

        # Motors (PWM output)
        self.hal.pin("MOTOR_L", board_pin=5, mode="pwm")
        self.hal.pin("MOTOR_R", board_pin=6, mode="pwm")

        # PID gains
        self.kp = 1.0
        self.ki = 0.05
        self.kd = 0.3
        self.integral = 0.0
        self.prev_error = 0.0
        self.base_speed = 0.5

    def tick(self):
        left = self.hal.read("IR_LEFT")
        right = self.hal.read("IR_RIGHT")

        # Error: positive = drifting right, negative = drifting left
        error = left - right

        # PID calculation
        self.integral += error * (1.0 / self.hz)
        derivative = (error - self.prev_error) * self.hz
        correction = self.kp * error + self.ki * self.integral + self.kd * derivative
        self.prev_error = error

        # Apply to motors
        left_speed  = max(0, min(1, self.base_speed - correction))
        right_speed = max(0, min(1, self.base_speed + correction))

        self.hal.write("MOTOR_L", left_speed)
        self.hal.write("MOTOR_R", right_speed)

        self.publish("/robot/line", {{
            "ir_left": round(left, 3),
            "ir_right": round(right, 3),
            "error": round(error, 3),
            "correction": round(correction, 3),
        }})

    def on_emergency_stop(self, reason):
        self.hal.write("MOTOR_L", 0)
        self.hal.write("MOTOR_R", 0)
        super().on_emergency_stop(reason)


robot = Robot("{project_name}")
robot.add_node(LineFollowerNode("line_follower", hz=50, priority=NodePriority.HIGH))
robot.start()

print("🏁 Line follower running — place robot on the line")
spin(robot)
''')


# ── 3. Obstacle Avoider ───────────────────────────────────────────────────
_t("obstacle-avoider", "Obstacle Avoider", "Navigate around obstacles using ultrasonic sensor", '''
"""
{project_name} — Obstacle Avoider
Generated by: neuros new {project_name} --template obstacle-avoider

Hardware required:
  - HC-SR04 ultrasonic sensor (TRIG + ECHO pins)
  - 2x DC motors with motor driver
  - Optional: servo for scanning
"""
from neuros import Robot, Node, spin, NodePriority
import time
import random


class UltrasonicNode(Node):
    """Read distance from HC-SR04 ultrasonic sensor."""

    def configure(self):
        self.hal.pin("TRIG", board_pin=9,  mode="output")
        self.hal.pin("ECHO", board_pin=10, mode="input")
        self.distance = 100.0  # cm

    def tick(self):
        # Trigger pulse
        self.hal.write("TRIG", 0)
        time.sleep(0.000002)
        self.hal.write("TRIG", 1)
        time.sleep(0.00001)
        self.hal.write("TRIG", 0)

        # In a real implementation, we'd time the echo pulse
        # For simulator, use injected sensor value
        raw = self.hal.read("ECHO")
        self.distance = raw * 400  # scale to cm

        self.publish("/robot/sensor/distance", {{
            "cm": round(self.distance, 1),
        }})


class NavigatorNode(Node):
    """Obstacle avoidance logic."""

    def configure(self):
        self.hal.pin("MOTOR_L", board_pin=5, mode="pwm")
        self.hal.pin("MOTOR_R", board_pin=6, mode="pwm")
        self.distance = 100.0
        self.state = "forward"

        self.subscribe("/robot/sensor/distance", self.on_distance)

    def on_distance(self, msg):
        self.distance = msg.data.get("cm", 100.0)

    def tick(self):
        if self.distance < 20:
            # Too close — back up and turn
            self.state = "avoiding"
            self.hal.write("MOTOR_L", 0)
            self.hal.write("MOTOR_R", 0)
            time.sleep(0.2)

            # Turn randomly left or right
            if random.random() > 0.5:
                self.hal.write("MOTOR_L", 0.0)
                self.hal.write("MOTOR_R", 0.5)
            else:
                self.hal.write("MOTOR_L", 0.5)
                self.hal.write("MOTOR_R", 0.0)
        else:
            self.state = "forward"
            self.hal.write("MOTOR_L", 0.5)
            self.hal.write("MOTOR_R", 0.5)

        self.publish("/robot/nav", {{"state": self.state, "distance": self.distance}})

    def on_emergency_stop(self, reason):
        self.hal.write("MOTOR_L", 0)
        self.hal.write("MOTOR_R", 0)
        super().on_emergency_stop(reason)


robot = Robot("{project_name}")
robot.add_node(UltrasonicNode("sonar", hz=10))
robot.add_node(NavigatorNode("navigator", hz=5, priority=NodePriority.HIGH))
robot.start()

print("🤖 Obstacle avoider running — place objects in front of robot")
spin(robot)
''')


# ── 4. Wheeled Bot ─────────────────────────────────────────────────────────
_t("wheeled-bot", "Wheeled Bot", "Basic 2-wheel differential drive robot", '''
"""
{project_name} — Wheeled Bot (Differential Drive)
Generated by: neuros new {project_name} --template wheeled-bot

Hardware required:
  - 2x DC motors + wheels
  - L298N motor driver
  - Battery pack
"""
from neuros import Robot, Node, spin, NodePriority


class DriveNode(Node):
    """Differential drive motor controller."""

    def configure(self):
        # Motor pins (PWM)
        self.hal.pin("LEFT_FWD",  board_pin=5, mode="pwm")
        self.hal.pin("LEFT_BWD",  board_pin=6, mode="pwm")
        self.hal.pin("RIGHT_FWD", board_pin=9, mode="pwm")
        self.hal.pin("RIGHT_BWD", board_pin=10, mode="pwm")

        # Motor enable pins
        self.hal.pin("LEFT_EN",  board_pin=3, mode="output")
        self.hal.pin("RIGHT_EN", board_pin=11, mode="output")

        # Enable motors
        self.hal.write("LEFT_EN", 1)
        self.hal.write("RIGHT_EN", 1)

        self.left_speed = 0.0
        self.right_speed = 0.0

        self.subscribe("/robot/motor/cmd", self.on_cmd)

    def on_cmd(self, msg):
        self.left_speed  = msg.data.get("left", 0.0)
        self.right_speed = msg.data.get("right", 0.0)

    def tick(self):
        # Left motor
        if self.left_speed >= 0:
            self.hal.write("LEFT_FWD", abs(self.left_speed))
            self.hal.write("LEFT_BWD", 0)
        else:
            self.hal.write("LEFT_FWD", 0)
            self.hal.write("LEFT_BWD", abs(self.left_speed))

        # Right motor
        if self.right_speed >= 0:
            self.hal.write("RIGHT_FWD", abs(self.right_speed))
            self.hal.write("RIGHT_BWD", 0)
        else:
            self.hal.write("RIGHT_FWD", 0)
            self.hal.write("RIGHT_BWD", abs(self.right_speed))

    def on_emergency_stop(self, reason):
        for pin in ("LEFT_FWD", "LEFT_BWD", "RIGHT_FWD", "RIGHT_BWD"):
            self.hal.write(pin, 0)
        super().on_emergency_stop(reason)


robot = Robot("{project_name}")
robot.add_node(DriveNode("drive", hz=100, priority=NodePriority.HIGH))
robot.start()

# Demo: drive forward for 2s, turn, repeat
import time

robot.publish("motor/cmd", {{"left": 0.5, "right": 0.5}})
print("⏩ Moving forward...")
time.sleep(2)

robot.publish("motor/cmd", {{"left": 0.5, "right": -0.5}})
print("↩️ Turning...")
time.sleep(1)

robot.publish("motor/cmd", {{"left": 0, "right": 0}})
print("⏹ Stopped")

spin(robot)
''')


