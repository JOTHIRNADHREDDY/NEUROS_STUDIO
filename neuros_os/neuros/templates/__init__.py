"""
{project_name} — Servo Sweep
Generated by: neuros new {project_name} --template servo-sweep

Hardware: Standard hobby servo on PWM pin
"""
from neuros import Robot, spin
import math

robot = Robot("{project_name}")
robot.start()

robot.pin("SERVO", pin=9, mode="pwm")

angle = 0
direction = 1

@robot.every(hz=30)
def sweep():
    global angle, direction
    angle += direction * 3  # 3 degrees per step

