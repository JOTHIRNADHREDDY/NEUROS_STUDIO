"""
{project_name} — Sensor Reader
Generated by: neuros new {project_name} --template sensor-reader

Reads multiple sensors and publishes values to the Neural Bus.
View live data in the NEUROS Studio web UI.
"""
from neuros import Robot, spin

robot = Robot("{project_name}")
robot.start()

# Configure sensors (adjust pins for your board)
robot.pin("LIGHT",    pin=34, mode="analog_in")    # LDR / photoresistor
robot.pin("TEMP",     pin=35, mode="analog_in")    # TMP36 temperature
robot.pin("POT",      pin=36, mode="analog_in")    # Potentiometer
robot.pin("BUTTON",   pin=2,  mode="pullup")       # Push button
robot.pin("STATUS",   pin=13, mode="output")       # Status LED

tick_count = 0

@robot.every(hz=10)
def read_sensors():
    global tick_count
    tick_count += 1

    light  = robot.read("LIGHT")
    temp_v = robot.read("TEMP")
    pot    = robot.read("POT")
    button = robot.read("BUTTON")

    # Convert raw ADC to meaningful values
    temp_c = (temp_v * 3.3 - 0.5) * 100  # TMP36 formula

    # Publish to Neural Bus
    robot.publish("sensors/all", {{
        "light":      round(light, 3),
        "temperature": round(temp_c, 1),
        "potentiometer": round(pot, 3),
        "button":     int(button),
        "tick":       tick_count,
    }})

    # Blink status LED to show we're alive
    if tick_count % 10 == 0:
        robot.toggle("STATUS")

    # Print to terminal
    if tick_count % 50 == 0:
        print(f"  Light: {{light:.2f}} | Temp: {{temp_c:.1f}}°C | Pot: {{pot:.2f}} | Button: {{button}}")

print("📊 Sensor reader running — publishing to Neural Bus")
spin(robot)
